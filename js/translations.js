// Obtener idioma actual de la cookie o usar español por defecto
function getCurrentLang() {
  const match = document.cookie.match(/site_lang=([^;]+)/);
  const lang = match ? match[1] : "es";
  // console.log("DEBUG getCurrentLang cookie match:", match, "lang:", lang);
  return lang;
}

// Función para obtener traducción
function t(key) {
  if (typeof translations === "undefined") {
    return key;
  }
  const lang = getCurrentLang();
  return translations[lang]?.[key] || translations["es"]?.[key] || key;
}

// Aplicar traducciones a elementos con data-i18n
function applyTranslations() {
  const lang = getCurrentLang();
  document.querySelectorAll("[data-i18n]").forEach(function (el) {
    const key = el.getAttribute("data-i18n");
    if (translations[lang] && translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });

  // Actualizar placeholders
  document.querySelectorAll("[data-i18n-placeholder]").forEach(function (el) {
    const key = el.getAttribute("data-i18n-placeholder");
    if (translations[lang] && translations[lang][key]) {
      el.placeholder = translations[lang][key];
    }
  });
}

// Aplicar traducciones al cargar la página
document.addEventListener("DOMContentLoaded", applyTranslations);
